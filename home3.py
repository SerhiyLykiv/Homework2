vuraz = "5 + 3 * 2"
pravul = 5 + 3 * 2  # Оскільки ми працюємо з виразом, можемо безпосередньо порахувати правильну відповідь

# Запитуємо користувача про відповідь
vidpov = input(f"Яка відповідь на вираз {vuraz}? ")

# Перевірка, чи ввів користувач правильну відповідь
try:
    vidpov = int(vidpov)  # Пробуємо перетворити введену відповідь в ціле число
    if vidpov == pravul:
        print("Правильно! Ви відповіли вірно.")
    else:
        print(f"Невірно. Правильна відповідь: {pravul}")
except ValueError:
    print("Будь ласка, введіть числову відповідь.")

