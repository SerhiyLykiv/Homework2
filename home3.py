def Dovgota_str(ved):
    # Якщо довжина рядка менша за 2, вивести повідомлення
    if len(ved) < 2:
        print("Рядок за короткий")
        return ""
    # В іншому випадку, беремо перші 2 та останні 2 символи
    return ved[:2] + ved[-2:]

# Тестування програми
ved = input("Введіть рядок: ")
print(Dovgota_str(ved))


